
//CSP is a graph class with a node
public class CSP {
	
	
	//a CSP has variables, domains and constraints
	Variable variable;
	Domain domain;
	Constraint constraint;
	
	//constructor
	public CSP(Variable variable, Domain domain, Constraint constraint){
		this.variable = variable;
		this.domain = domain;
		this.constraint = constraint;
	}
	
	
	

}
